package org.eclipse.apoapsis.ortserver.core.queries

/**
 * An interface for queries. Queries only read data and do not modify the state of the system. They are handled by a
 * [QueryHandler]. The [RETURN_VALUE] type parameter specifies the return value of the query.
 */
interface Query<RETURN_VALUE> {
    val traceId: String
}
